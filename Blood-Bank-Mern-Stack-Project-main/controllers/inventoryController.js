// GET DONAR REOCRDS 
const getDonarsController = async (req, res) => {  //  in donar page
  try {
    const organisation = req.body.userId;
    //find donars id
    const donorId = await inventoryModel.distinct("donar", {  // donar from inventory model and only org can see donor list
      organisation,
    });
    // console.log(donorId);
    const donars = await userModel.find({ _id: { $in: donorId } }); // $in -> if both are same

    return res.status(200).send({
      success: true,
      message: "Donar Record Fetched Successfully",
      donars,
    });
  } catch (error) {
    console.log(error);
    return res.status(500).send({
      success: false,
      message: "Error in Donar records",
      error,
    });
  }
};

